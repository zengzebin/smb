package com.ssxt.model;

public class Province {
    private String provinceName;
    private String provinceBoundary;
	public String getProvinceBoundary() {
		return provinceBoundary;
	}
	public void setProvinceBoundary(String provinceBoundary) {
		this.provinceBoundary = provinceBoundary;
	}
	public String getProvinceName() {
		return provinceName;
	}
	public void setProvinceName(String provinceName) {
		this.provinceName = provinceName;
	}
	 
}
