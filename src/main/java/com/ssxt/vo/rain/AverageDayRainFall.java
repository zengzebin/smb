package com.ssxt.vo.rain;

public class AverageDayRainFall {
	private String stationNo;//测站
	private double averageDayRainFall; //平均日雨量
	public String getStationNo() {
		return stationNo;
	}
	public void setStationNo(String stationNo) {
		this.stationNo = stationNo;
	}
	public double getAverageDayRainFall() {
		return averageDayRainFall;
	}
	public void setAverageDayRainFall(double averageDayRainFall) {
		this.averageDayRainFall = averageDayRainFall;
	}
	 

}
