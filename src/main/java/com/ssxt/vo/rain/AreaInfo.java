package com.ssxt.vo.rain;

public class AreaInfo {
		private String stationNo;  //测站编码
		private	String stationName;  //测站名称
		private	String adminAreaName;  //行政区划名称
		private	String valleyName;   //流域名称
		private	String stationType; //测站类型
		
		
	 
			public String getStationNo() {
				return stationNo;
			}
			public void setStationNo(String stationNo) {
				this.stationNo = stationNo;
			}
			public String getStationName() {
				return stationName;
			}
			public void setStationName(String stationName) {
				this.stationName = stationName;
			}
			public String getAdminAreaName() {
				return adminAreaName;
			}
			public void setAdminAreaName(String adminAreaName) {
				this.adminAreaName = adminAreaName;
			}
			public String getValleyName() {
				return valleyName;
			}
			public void setValleyName(String valleyName) {
				this.valleyName = valleyName;
			}
			public String getStationType() {
				return stationType;
			}
			public void setStationType(String stationType) {
				this.stationType = stationType;
			}

}
